--------------CREATE TABLES-----------
CREATE TABLE POSITION
(
	ID_POSITION INT NOT NULL PRIMARY KEY,
    TITLE VARCHAR(255),
)
GO

CREATE TABLE STAFF
(
    ID_STAFF INT NOT NULL PRIMARY KEY,
    NAME_STAFF NVARCHAR(255),
    PHONE VARCHAR(10),
    EMAIL VARCHAR(255),
    USERNAME VARCHAR(255),
    PASSWORDS VARCHAR(255),
    ID_POSITION INT,
    REGISTERED_AT DATETIME,
)
GO

CREATE TABLE CUSTOMER
(
    ID_CUSTOMER INT NOT NULL PRIMARY KEY,
    NAMES NVARCHAR(255),
    PHONE VARCHAR(10),
    VISITED_AT DATETIME
)
GO

CREATE TABLE CUSTOMER_PROMO
(
    ID_CUSTOMER INT,
    ID_PROMO INT,
    USED INT,
)
GO

CREATE TABLE TABLES
(
    ID_TABLE INT NOT NULL PRIMARY KEY,
    FLOOR_NUMBER VARCHAR(255),
    PEOPLE_NUMBER INT,
    STATUSS INT,
)
GO

CREATE TABLE INVOICE
(
    ID_INVOICE INT NOT NULL PRIMARY KEY,
    CREATED_AT DATETIME,
    ID_TABLE INT,
    ID_STAFF INT,
    ID_PROMO INT,
    TAX FLOAT,
    DISCOUNT FLOAT,
    TOTAL_PRICE INT,
    NET_PRICE INT,
    MONEY_RECEICED INT,
    MONEY_CHANGE INT,
)
GO

CREATE TABLE PROMO
(
    ID_PROMO INT NOT NULL PRIMARY KEY,
    NAMES VARCHAR(255),
    PERCENTS FLOAT,
    MIN_PRICE INT,
    PRE_CONDITION INT,
    EXPIRED DATETIME,
    AVAIABLE INT, 
)
GO

CREATE TABLE ORDERS
(
    ID_ORDER INT NOT NULL PRIMARY KEY,
    ID_TABLE INT,
    CREATED_AT DATETIME,
    UPDATED_AT DATETIME,
    STATUSS INT,
)
GO

CREATE TABLE INVOICE_DETAIL
(
    ID_ORDER INT,
    ID_INVOICE INT,
)
GO

CREATE TABLE ORDER_DETAIL
(
    ID_ORDER INT,
    ID_DISH INT,
    QUANTITY INT,
    NOTE VARCHAR(255),
)
GO

CREATE TABLE DISH
(
    ID_DISH INT NOT NULL PRIMARY KEY,
    NAMES NVARCHAR(255),
    ID_TYPE INT,
    DESCRIPTIONS NVARCHAR(255),
    RATION INT,
    STATUSS INT,
    PRICE INT,
    DISCOUNT INT,
    TIME_SLOT VARCHAR(255),
)
GO

CREATE TABLE TYPE_OF_DISH
(
    ID_TYPE INT NOT NULL PRIMARY KEY,
    NAMES NVARCHAR(255),
)
GO

CREATE TABLE IMAGES
(
    ID_IMAGE INT NOT NULL PRIMARY KEY,
    ID_DISH INT,
    SOURCE VARCHAR(255),
)
GO

CREATE TABLE RECCOMMEND_DISH
(
    ID_DISH INT,
)
GO

